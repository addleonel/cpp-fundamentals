#include <iostream>

using namespace std;

int main() {
   // Initializing == Declaring + assigning
   int a = 5;

   // Declaring variables
   int b, c;
   int result;

   // Assigning values
   b = 2;
   c = 4;
   
   // Process
   result = a + b + c;
   
   // print out the result
   cout<<result;  // 11

   return 0;
}
